    <h4>O que é Oxws?</h4>
    <p>Oxws, sigla para Oxsys Websites, é uma ferramenta que posibilita criar Websites e uma forma simples, rápida e eficiente.</p>

    <h4>Onde posso usar Oxws</h4>
    <p>Basicamente para criar Websites simples com informações sobre o seu negócio, serviços, produtos, contato e administração. Porém os módulos podem ser customizados para atender a necessidades mais complexas como gerenciamento de conteúdo, mídia, produtos, clientes, etc.</p>
  </div>

  <div class="col-sm-6">
    <h4>História</h4>
    <p>Oxws nasceu da necessidade de criar Websites rapidamente, atendendo funcionalidades simples como reutilização de blocos de html e integração com banco de dados. Foi utilizado como um framework proprietário até que houve a vontade de transforma-lo em um projeto open source. Passou por um processo de remodelagem e refactoring para ser disponibilizado publicamente sob a licença GPL-3.</p>

    <h4>Oxws para desenvolvedores</h4>
    <p>Mantém o código do seu projeto organizado e possibilita uma melhor integração da equipe de desenvolvedores.</p>
